#include "libft.h"

char		*ft_strdup(const char *str)
{
	int		i;
	char	*dup;

	if (!(dup = (char*)malloc(ft_strlen(str) + 1)))
		return (NULL);
	i = -1;
	while (str[++i])
		dup[i] = str[i];
	dup[i] = '\0';
	return (dup);
}
